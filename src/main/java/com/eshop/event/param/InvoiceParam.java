package com.eshop.event.param;

import com.jfinal.plugin.activerecord.Record;

public class InvoiceParam extends EventParam {
	
	private int orderId;
	private Record invoice;
	
	public int getOrderId() {
		return orderId;
	}
	
	public void setOrderId(int orderId) {
		this.orderId = orderId;
	}
	
	public Record getInvoice() {
		return invoice;
	}
	
	public void setInvoice(Record invoice) {
		this.invoice = invoice;
	}
	
}
